/**
 * string_toupper - Changes all characters of a string in lwercase to uppercase
 * @s: Pointer to String to be converted
 *
 * Return: String
 */

char *string_toupper(char *s)
{
	int i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] >= 'a' && s[i] <= 'z')
		{
			s[i] = s[i] - 32;
		}
		i++;
	}
	return (s);
}
